Canvas::Canvas()
    : window(nullptr),
      should_render(true),
      render_thread(&Canvas::main_loop, this) {
    ascii_font.loadFromMemory(ascii_ttf, ascii_ttf_len);
}

Canvas::~Canvas() {
    should_render = false; // ends main_loop next frame
    clear(); // nothing left to render
    render_thread.join(); // wait for main_loop to end
    delete window;
}

int Canvas::add_circle(int x, int y, int radius,
                       sf::Color color) {
    auto circle = make_shared<sf::CircleShape>(radius);
    circle->setPosition(x, y);
    circle->setFillColor(color);
    lock_guard<mutex> guard(render_lock);
    int id = nextfree_shape++;
    shapes[id] = static_pointer_cast<sf::Shape>(circle);
    return id;
}

int Canvas::add_line(int x, int y, int tox, int toy,
                     sf::Color color, int width) {
    double dx = tox-x, dy = toy-y;
    double length = sqrt(dx*dx + dy*dy);
    // openclassrooms.com/forum/sujet/sfml-2-0-tracer-des-lignes-43759
    double alpha = atan2(dy, dx) * (180.0/3.141592653589793238463);
    auto rectangle =
        make_shared<sf::RectangleShape>(sf::Vector2f(length, width));
    rectangle->setPosition(x, y);
    rectangle->setFillColor(color);    
    rectangle->setOrigin(0, width/2);
    rectangle->setRotation(alpha);
    lock_guard<mutex> guard(render_lock);
    int id = nextfree_shape++;
    shapes[id] = static_pointer_cast<sf::Shape>(rectangle);
    return id;    
}

int Canvas::add_rectangle(int x, int y, int width, int height,
                          sf::Color color) {
    auto rectangle =
        make_shared<sf::RectangleShape>(sf::Vector2f(width, height));
    rectangle->setPosition(x, y);
    rectangle->setFillColor(color);
    lock_guard<mutex> guard(render_lock);
    int id = nextfree_shape++;
    shapes[id] = static_pointer_cast<sf::Shape>(rectangle);
    return id;    
}

int Canvas::add_text(int x, int y, const char *s, 
                     int px, sf::Color color) {
    sf::Text text;
    text.setFont(ascii_font);
    text.setString(s);
    text.setCharacterSize(px); // in pixels, not points!
    text.setFillColor(color);
    text.setPosition(x, y);
    lock_guard<mutex> guard(render_lock);
    int id = nextfree_text--;
    texts[id] = text; // for text a copy
    return id;
}

bool Canvas::change_text(int id, const char *s) {
    if (texts.find(id) == texts.end()) {
        return false;
    } 
    sf::Text &text = texts[id];
    text.setString(s);
    return true;
}

bool Canvas::move_to(int id, int x, int y) {
    auto it = shapes.find(id);
    if (it == shapes.end()) {
        return false;
    }
    lock_guard<mutex> guard(render_lock);
    it->second->setPosition(x, y);
    return true;
}

bool Canvas::move_by(int id, int dx, int dy) {
    auto it = shapes.find(id);
    if (it == shapes.end()) {
        return false;
    }
    lock_guard<mutex> guard(render_lock);
    auto pos = it->second->getPosition();
    it->second->setPosition(pos.x+dx, pos.y+dy);
    return true;
}

int Canvas::get_posx(int id) {
    auto it = shapes.find(id);
    if (it == shapes.end()) {
        return -1;
    }
    return it->second->getPosition().x;    
}

int Canvas::get_posy(int id) {
    auto it = shapes.find(id);
    if (it == shapes.end()) {
        return -1;
    }
    return it->second->getPosition().y;
}

void Canvas::clear() {
    lock_guard<mutex> guard(render_lock);
    shapes.clear(); // auto delete of shapes 
    texts.clear();
}

void Canvas::quit(int ms) {
    const int step=20; // ms to sleep and wait
    while (ms > 0 && is_alive()) { // ugly busy wait, captures close window
        schlafeMs(step); // dampened
        ms -= step;
    }
    should_render = false;
    // musn't close window, not the same thread
}

void Canvas::main_loop() {
    window = new sf::RenderWindow(sf::VideoMode(DEFAULT_WIDTH, DEFAULT_HEIGHT),
                                  DEFAULT_TITLE);
    window->setVerticalSyncEnabled(true); // adhere to monitor refresh rate
    while (should_render && window->isOpen()) {
        // MACOSX note: Apparently we have to call everything UI related
        // in the main thread (not a render thread but the main thread?)
        // very unfortunate -> todo
        handle_events();
        window->clear();
        render();
        window->display();
    }    
}

void Canvas::handle_events() {
    sf::Event event;
    while (window->pollEvent(event)) {
        switch (event.type) {
        case sf::Event::Closed:
            should_render = false;
            window->close();
            break; 
        default: // unknown/unhandled event
            break;
        }
    }
}

void Canvas::render() {
    lock_guard<mutex> guard(render_lock);
    for (auto const &shape : shapes) {
        window->draw(*shape.second);
    }
    for (auto const &text: texts) {
        window->draw(text.second);
    }
}



Pixels::Pixels(int w)
    : window(nullptr),
      should_render(true),
      render_thread(&Pixels::main_loop, this) {
    ascii_font.loadFromMemory(ascii_ttf, ascii_ttf_len);
    if (w < 0 || w > 600) {
        throw ooptool_exception("Pixels::wrong width dimension", w);
    }
    _width = w;
    _height = w*2/3;
    bitmap.create(_width, _height, sf::Color::Black);
}

Pixels::~Pixels() {
    should_render = false; // ends main_loop next frame
    clear(); // nothing left to render
    render_thread.join(); // wait for main_loop to end
    delete window;
}

bool Pixels::set(int x, int y, sf::Color color) {
    if (x < 0 || x >= _width || y < 0 || y >= _height) {
        return false;
    }
    bitmap.setPixel(x, y, color);
    return true;
}

int Pixels::add_text(int x, int y, const char *s, 
                     int px, sf::Color color) {
    sf::Text text;
    text.setFont(ascii_font);
    text.setString(s);
    text.setCharacterSize(px); // in pixels, not points!
    text.setFillColor(color);
    text.setPosition(x+_width, y);
    lock_guard<mutex> guard(render_lock);
    int id = nextfree_text--;
    texts[id] = text; // for text a copy
    return id;
}

bool Pixels::change_text(int id, const char *s) {
    if (texts.find(id) == texts.end()) {
        return false;
    } 
    sf::Text &text = texts[id];
    text.setString(s);
    return true;
}

void Pixels::clear() {
    lock_guard<mutex> guard(render_lock);
    for (int x=0; x < _width; x+=1) {
        for (int y=0; y < _height; y+=1) {
            bitmap.setPixel(x, y, sf::Color::Black);
        }
    }
}

void Pixels::clear_all() {
    lock_guard<mutex> guard(render_lock);
    clear();
    texts.clear();
}

