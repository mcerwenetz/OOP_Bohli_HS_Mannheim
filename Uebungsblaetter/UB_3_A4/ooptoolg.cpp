void Pixels::render() {
    lock_guard<mutex> guard(render_lock);
    texture.loadFromImage(bitmap);
    sprite.setTexture(texture, true);
    window->draw(sprite);
    // there are some texts at the right side
    for (auto const &text: texts) {
        window->draw(text.second);
    }

}


